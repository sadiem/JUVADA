int _KEY::KeyCheck()
{
	//前情報を保存
	for(int i = 0; i < sizeof(KeyTblAft) / sizeof(BYTE); i++)
		KeyTblAft[i] = KeyTblNow[i];

	//キーボード情報の取得の有無
	if(!GetKeyboardState(KeyTblNow)){
		return -1;
	}
	return 0;
}


